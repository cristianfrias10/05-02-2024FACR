    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        String idText = txtElegir.getText();
        txtElegir.setText("");

        try {
            int id = Integer.parseInt(idText);
            boolean encontrado = false;
            for (int i = 0; i < mate.size(); i++) {
                if (mate.get(i).getId() == id) {
                    mostrarDatos(i);
                    encontrado = true;
                    break;
                }
            }
            if (!encontrado) {
                javax.swing.JOptionPane.showMessageDialog(this, "ID no encontrada.");
            }
        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor ingrese un numero valido en la ID.");
        }
    }//GEN-LAST:event_BuscarActionPerformed

    private void BorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BorrarActionPerformed
        String idText = txtElegir.getText();

        try {
            int id = Integer.parseInt(idText);
            boolean encontrado = false;
            for (int i = 0; i < mate.size(); i++) {
                if (mate.get(i).getId() == id) {
                    mate.remove(i);
                    encontrado = true;
                    break;
                }
            }
            if (encontrado) {
                javax.swing.JOptionPane.showMessageDialog(this, "Materia borrada exitosamente.");

                try {
                    java.io.FileOutputStream fos = new java.io.FileOutputStream("Materias");
                    java.io.ObjectOutputStream oos = new java.io.ObjectOutputStream(fos);
                    oos.writeObject(mate);
                    oos.close();
                } catch (java.io.IOException ex) {
                    System.out.println("Error al guardar datos en el archivo: " + ex.toString());
                }

                lblId.setText("ID");
                lblNombre.setText("Nombre");
                lblProfesor.setText("Profesor");
                lblCreditos.setText("Creditos");
                lblDias.setText("Dias Inpuestos");
                lblHoras.setText("Horas a la Semana");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "ID no encontrada.");
            }
        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor ingrese un nÃºmero vÃ¡lido en la ID.");
        }
    }//GEN-LAST:event_BorrarActionPerformed

