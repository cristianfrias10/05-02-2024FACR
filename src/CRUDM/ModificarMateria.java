    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        String idModificar = txtId.getText().trim();
        String nuevoNombre = txtNombre.getText().trim();
        String nuevoProfesor = txtProfesor.getText().trim();
        String nuevosCreditos = txtCreditos.getText().trim();
        String nuevosDiasI = txtDias.getText().trim();
        String nuevasHorasS = txtHoras.getText().trim();

        Clases.Materia materiaModificar = null;
        for (Clases.Materia materia : mate) {
            if (String.valueOf(materia.getId()).equals(idModificar)) {
                materiaModificar = materia;
                break;
            }
        }

        if (!nuevoNombre.isEmpty()) {
            materiaModificar.setNombre(nuevoNombre);
        }
        if (!nuevoProfesor.isEmpty()) {
            materiaModificar.setProfesor(nuevoProfesor);
        }
        if (!nuevosCreditos.isEmpty()) {
            int nuevosCred = Integer.parseInt(nuevosCreditos);
            materiaModificar.setCreditos((byte) nuevosCred);
        }
        if (!nuevosDiasI.isEmpty()) {
            materiaModificar.setDiasI(nuevosDiasI);
        }
        if (!nuevasHorasS.isEmpty()) {
            int nuevoHor = Integer.parseInt(nuevasHorasS);
            materiaModificar.setHorasS((byte) nuevoHor);
        }

        txtId.setText("");
        txtNombre.setText("");
        txtProfesor.setText("");
        txtCreditos.setText("");
        txtDias.setText("");
        txtHoras.setText("");

        try {
            java.io.FileOutputStream fos = new java.io.FileOutputStream("Materias");
            java.io.ObjectOutputStream oos = new java.io.ObjectOutputStream(fos);
            oos.writeObject(mate);
            oos.close();
            javax.swing.JOptionPane.showMessageDialog(this, "Los datos se han modificado y guardado correctamente.");
        } catch (java.io.IOException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar los datos modificados: " + ex.toString());
        }
    }//GEN-LAST:event_ModificarActionPerformed

