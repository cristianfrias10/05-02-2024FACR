    private void mostrarDatos(int index) {
        if (index >= 0 && index < mate.size()) {
            Clases.Materia materia = mate.get(index);
            lblId.setText("Matricula: " + materia.getId());
            lblNombre.setText("Nombre: " + materia.getNombre());
            lblProfesor.setText("Apellidos: " + materia.getProfesor());
            lblCreditos.setText("Edad: " + materia.getCreditos());
            lblDias.setText("Carrera: " + materia.getDiasI());
            lblHoras.setText("Semestre: " + materia.getHorasS());
        }
    }

