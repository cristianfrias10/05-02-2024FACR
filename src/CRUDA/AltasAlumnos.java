    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        boolean datosIngresados = txtMatricula.getText().isEmpty() || !txtNombre.getText().isEmpty() || !txtApellidos.getText().isEmpty() || !txtEdad.getText().isEmpty() || !txtCarrera.getText().isEmpty() || !txtSemestre.getText().isEmpty();

        if (!datosIngresados) {
            javax.swing.JOptionPane.showMessageDialog(this, "No se han ingresado datos para guardar.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (txtMatricula.getText().isEmpty() || txtNombre.getText().isEmpty() || txtApellidos.getText().isEmpty() || txtEdad.getText().isEmpty() || txtCarrera.getText().isEmpty() || txtSemestre.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Todos los campos deben ser llenados.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int mat = Integer.parseInt(txtMatricula.getText());
        String nom = txtNombre.getText();
        String ap = txtApellidos.getText();
        byte ed = Byte.parseByte(txtEdad.getText());
        String car = txtCarrera.getText();
        byte sem = Byte.parseByte(txtSemestre.getText());
        Clases.Alumno alum = new Clases.Alumno(mat, nom, ap, ed, car, sem);

        try {
            java.io.FileInputStream fis = new java.io.FileInputStream("Alumnos");
            java.io.ObjectInputStream ois = new java.io.ObjectInputStream(fis);
            alums = (java.util.ArrayList<Clases.Alumno>) ois.readObject();
            ois.close();
        } catch (java.io.IOException | ClassNotFoundException ex) {
            //Si no hay datos existentes, no hacer nada
        }

        alums.add(alum);

        try {
            java.io.FileOutputStream fos = new java.io.FileOutputStream("Alumnos");
            java.io.ObjectOutputStream oos = new java.io.ObjectOutputStream(fos);
            oos.writeObject(alums);
            oos.close();

            txtMatricula.setText("");
            txtNombre.setText("");
            txtApellidos.setText("");
            txtEdad.setText("");
            txtCarrera.setText("");
            txtSemestre.setText("");

        } catch (java.io.IOException es) {
            System.out.println("Error al Guardar en el archivo " + es.toString());
        }
    }//GEN-LAST:event_GuardarActionPerformed

    private void MostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostrarActionPerformed
        try {
            java.io.FileInputStream fis = new java.io.FileInputStream("Alumnos");
            java.io.ObjectInputStream ois = new java.io.ObjectInputStream(fis);
            alums = (java.util.ArrayList) ois.readObject();
            ois.close();
        } catch (java.io.IOException | ClassNotFoundException es) {
            System.out.println("Error al Guardar en el archivo " + es.toString());
        }
        for (Clases.Alumno a : alums) {
            System.out.println(a);
        }

        this.dispose();

        reportes.setVisible(true);
    }//GEN-LAST:event_MostrarActionPerformed

