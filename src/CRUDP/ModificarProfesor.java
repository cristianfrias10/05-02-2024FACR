    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        String idModificar = txtId.getText().trim();
        String nuevoNombre = txtNombre.getText().trim();
        String nuevosApellidos = txtApellidos.getText().trim();
        String nuevaEdadStr = txtEdad.getText().trim();
        String nuevaCarrera = txtCarrera.getText().trim();
        String nuevoSemestreStr = txtSemestre.getText().trim();

        Clases.Profesor profesorModificar = null;
        for (Clases.Profesor profesor : prof) {
            if (String.valueOf(profesor.getId()).equals(idModificar)) {
                profesorModificar = profesor;
                break;
            }
        }

        if (!nuevoNombre.isEmpty()) {
            profesorModificar.setNombre(nuevoNombre);
        }
        if (!nuevosApellidos.isEmpty()) {
            profesorModificar.setApellidos(nuevosApellidos);
        }
        if (!nuevaEdadStr.isEmpty()) {
            int nuevaEdad = Integer.parseInt(nuevaEdadStr);
            profesorModificar.setEdad((byte) nuevaEdad);
        }
        if (!nuevaCarrera.isEmpty()) {
            profesorModificar.setCarrera(nuevaCarrera);
        }
        if (!nuevoSemestreStr.isEmpty()) {
            int nuevoSemestre = Integer.parseInt(nuevoSemestreStr);
            profesorModificar.setSemestre((byte) nuevoSemestre);
        }

        txtId.setText("");
        txtNombre.setText("");
        txtApellidos.setText("");
        txtEdad.setText("");
        txtCarrera.setText("");
        txtSemestre.setText("");

        try {
            java.io.FileOutputStream fos = new java.io.FileOutputStream("Profesores");
            java.io.ObjectOutputStream oos = new java.io.ObjectOutputStream(fos);
            oos.writeObject(prof);
            oos.close();
            javax.swing.JOptionPane.showMessageDialog(this, "Los datos se han modificado y guardado correctamente.");
        } catch (java.io.IOException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar los datos modificados: " + ex.toString());
        }
    }//GEN-LAST:event_ModificarActionPerformed

