    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        String idBuscar = txtElegir.getText().trim();

        if (idBuscar.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor ingrese una ID a buscar.");
        } else {
            boolean encontrado = false;
            int indiceProfesor = -1;
            for (int i = 0; i < prof.size(); i++) {
                if (String.valueOf(prof.get(i).getId()).equals(idBuscar)) {
                    encontrado = true;
                    indiceProfesor = i;
                    break;
                }
            }

            if (encontrado) {
                mostrarDatos(indiceProfesor);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "La Id especificada no existe en la base de datos.");
            }

            txtElegir.setText("");
        }
    }//GEN-LAST:event_BuscarActionPerformed

