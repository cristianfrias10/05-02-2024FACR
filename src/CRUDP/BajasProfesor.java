    private void mostrarDatos(int index) {
        if (index >= 0 && index < prof.size()) {
            Clases.Profesor profesor = prof.get(index);
            lblId.setText("ID: " + profesor.getId());
            lblNombre.setText("Nombre: " + profesor.getNombre());
            lblApellidos.setText("Apellidos: " + profesor.getApellidos());
            lblEdad.setText("Edad: " + profesor.getEdad());
            lblCarrera.setText("Carrera: " + profesor.getCarrera());
            lblSemestre.setText("Semestre: " + profesor.getSemestre());
        }
    }

