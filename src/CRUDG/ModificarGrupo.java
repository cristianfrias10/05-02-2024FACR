    private void mostrarDatos(int index) {
        if (index >= 0 && index < grup.size()) {
            Clases.Grupo grupo = grup.get(index);
            lblId.setText("Matricula: " + grupo.getId());
            lblGrupo.setText("Grupo: " + grupo.getGrupo());
        }
    }

