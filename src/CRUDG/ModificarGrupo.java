    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        String idBuscar = txtElegir.getText().trim();

        if (idBuscar.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor ingrese una ID a buscar.");
        } else {
            boolean encontrado = false;
            int indiceGrupo = -1;
            for (int i = 0; i < grup.size(); i++) {
                if (String.valueOf(grup.get(i).getId()).equals(idBuscar)) {
                    encontrado = true;
                    indiceGrupo = i;
                    break;
                }
            }

            if (encontrado) {
                mostrarDatos(indiceGrupo);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "La ID especificada no existe en la base de datos.");
            }

            txtElegir.setText("");
        }
    }//GEN-LAST:event_BuscarActionPerformed

    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        String idModificar = txtId.getText().trim();
        String nuevoGrupo = txtGrupo.getText().trim();

        Clases.Grupo grupoModificar = null;
        for (Clases.Grupo grupo : grup) {
            if (String.valueOf(grupo.getId()).equals(idModificar)) {
                grupoModificar = grupo;
                break;
            }
        }

        if (!nuevoGrupo.isEmpty()) {
            grupoModificar.setGrupo(nuevoGrupo);
        }

        txtId.setText("");
        txtGrupo.setText("");

        try {
            java.io.FileOutputStream fos = new java.io.FileOutputStream("Grupos");
            java.io.ObjectOutputStream oos = new java.io.ObjectOutputStream(fos);
            oos.writeObject(grup);
            oos.close();
            javax.swing.JOptionPane.showMessageDialog(this, "Los datos se han modificado y guardado correctamente.");
        } catch (java.io.IOException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar los datos modificados: " + ex.toString());
        }
    }//GEN-LAST:event_ModificarActionPerformed

